
# vytvoří řetězec ahoj svete a vypíše jej
retezec = "Ahoj světe, ahoj pythone!"
print(f"Původní věta: {retezec}")

# nahradí slova ahoj za čau a vypíše pozměněný řetězec
zmeneny = retezec.replace("Ahoj", "Čau")
zmeneny = zmeneny.replace("ahoj", "čau")
print(f"Aktualizovaný řetězec: {zmeneny}")
