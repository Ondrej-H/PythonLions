
# vytvoří seznam a vypíše jej
seznam = [4, 1, 5, 2, 3]
print(f"Můj seznam je: {seznam}")

# vyhledá největší číslo v seznamu a vypíše jej
nejvyssi_cislo = max(seznam)
print(f"Nejvyšší číslo ze seznamu {seznam} je: {nejvyssi_cislo}")

# vyhledá nejnižší číslo v seznamu a vypíše jej
nejvyssi_cislo = min(seznam)
print(f"Nejnižší číslo ze seznamu {seznam} je: {nejvyssi_cislo}")

